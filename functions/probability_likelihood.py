"""
似然函数(likelihood function)与概率函数(probability function)
对于函数 P(x∣θ) 有两个参数: x表示某一个具体的数据；θ表示模型的参数
如果 θ 是已知确定的,x 是变量,这个函数叫做《概率函数》; 如果 x 是已知确定的,θ 是变量,这个函数叫做《似然函数》
通俗来说：
概率：已知参数,预测结果;
似然：已知结果,估计参数
"""

"""
似然函数公式: L(θ|X) = f(X|θ)
如果 X 是离散分布, 则 f(X|θ) 是概率质量函数(PMF)。其值（在数值上）等于给定参数θ后变量x的概率,即 L(θ|x)=P(X=x|θ)
如果 X 是连续分布, 则 f(X|θ) 是概率密度函数(PDF)
"""

