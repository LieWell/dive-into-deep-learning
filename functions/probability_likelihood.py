"""
概率密度函数(probability density function,简称 PDF)
用于连续型随机变量,表示在某个取值处的概率密度,而不是具体的概率值。对于连续型变量,概率密度函数的积分可以给出某个区间内随机变量取值的概率。
举个例子,考虑标准正态分布,其概率密度函数为：
f(x) = (1 / sqrt(2π)) * exp(-(x^2) / 2)
这里,f(x)表示随机变量X在取值x处的概率密度。由于正态分布是连续型分布,我们可以通过对概率密度函数进行积分来计算X落在某个区间的概率。
"""

"""
概率质量函数(probability mass function简称 PMF)
用于离散型随机变量,它描述了每个可能取值的概率。
例如,考虑一个均匀骰子,其概率质量函数为：
f(x) = 1/6,其中 x 取值为 1, 2, 3, 4, 5, 6
这里,f(x)表示随机变量X取值为x的概率。由于骰子是离散型变量,每个可能取值的概率都是1/6。
"""

